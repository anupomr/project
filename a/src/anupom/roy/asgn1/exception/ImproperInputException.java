package anupom.roy.asgn1.exception;

public class ImproperInputException extends Exception {	
	private static final long serialVersionUID = 1L;

	public ImproperInputException() {
		
	}

	public ImproperInputException(String arg0) {
		super(arg0);
		
	}

	public ImproperInputException(Throwable arg0) {
		super(arg0);
		
	}

	public ImproperInputException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public ImproperInputException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
